-- Primeiro testa o select para conferir se vai puxar os dados corretamente

SELECT
    PONTE.*
FROM dbo.CLIENTES_TRATADOS AS PONTE
WHERE
    ISNULL(NULLIF(LTRIM(RTRIM(PONTE.CPFCNPJ)), ''), '') <> ''
    AND NOT EXISTS (
        SELECT 1
        FROM dbo.CADASTRO_PESSOAS AS PESSOA
        WHERE PESSOA.CPFCNPJ = PONTE.CPFCNPJ
    );

